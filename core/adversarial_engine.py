"""
Adversarial Engine & Red-Teaming Module
Integrates: Scikit-learn, NumPy, Pandas
Provides: Deep-dive diagnostics for model robustness.
"""

import numpy as np
import pandas as pd
from typing import Dict, Any
from sklearn.ensemble import IsolationForest

class AdversarialEngine:
    def __init__(self):
        pass

    def check_data_leakage(self, df: pd.DataFrame, target_col: str) -> Dict[str, Any]:
        """
        Detects features that might be proxying the target.
        """
        results = {"high_risk_cols": [], "correlations": {}}
        
        # Calculate correlations with target
        if df[target_col].dtype == 'object' or df[target_col].nunique() < 10:
            # Classification: use label encoding for correlation check
            target_encoded = pd.factorize(df[target_col])[0]
        else:
            target_encoded = df[target_col]

        for col in df.columns:
            if col == target_col:
                continue
            
            try:
                if df[col].dtype in ['int64', 'float64']:
                    corr = np.abs(np.corrcoef(df[col], target_encoded)[0, 1])
                    results["correlations"][col] = corr
                    if corr > 0.95:
                        results["high_risk_cols"].append(col)
            except Exception:
                results["correlations"][col] = 0.0
        
        return results

    def detect_adversarial_vulnerability(self, X: pd.DataFrame) -> Dict[str, Any]:
        """
        Uses Isolation Forest to find training points that might be "out of distribution"
        and susceptible to noise.
        """
        # Select numeric only for this check
        X_num = X.select_dtypes(include=[np.number])
        if X_num.empty:
            return {"vulnerability_score": 0, "n_outliers": 0}

        iso = IsolationForest(contamination=0.05, random_state=42)
        outliers = iso.fit_predict(X_num)
        
        n_outliers = int((outliers == -1).sum())
        vulnerability_score = n_outliers / len(X)
        
        return {
            "vulnerability_score": vulnerability_score,
            "n_outliers": n_outliers,
            "risk_level": "High" if vulnerability_score > 0.1 else "Low"
        }

    def generate_red_team_report(self, df: pd.DataFrame, target_col: str) -> str:
        """
        Produces a technical report for the agents.
        """
        leakage = self.check_data_leakage(df, target_col)
        robustness = self.detect_adversarial_vulnerability(df.drop(columns=[target_col]))
        
        report = f"""
### 🛡️ Red-Team Diagnostic Report
**Autonomously Generated by: AdversarialEngine**

1. **Leakage Analysis**:
   - High Risk Columns: {leakage['high_risk_cols'] if leakage['high_risk_cols'] else 'None detected.'}
   - Max Feature Correlation: {max(leakage['correlations'].values()) if leakage['correlations'] else 0:.4f}

2. **Robustness Check**:
   - Adversarial Vulnerability Score: {robustness['vulnerability_score']:.4f}
   - Theoretical Outlier Contamination: {robustness['n_outliers']} samples
   - Risk Level: {robustness['risk_level']}

3. **Suggested Defense**:
   - {'Drop high-risk columns immediately.' if leakage['high_risk_cols'] else 'No immediate leakage detected.'}
   - {'Consider Robust Scaling or Outlier Clipping for stability.' if robustness['risk_level'] == "High" else 'Standard preprocessing should suffice.'}
"""
        return report

